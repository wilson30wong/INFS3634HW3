package com.example.infs3634hw3;

public class CatDetail {

    //breeds = cat information
    private Cat[] breeds;
    private String id;
    //url = image
    private String url;

    public Cat[] getBreeds() {
        return breeds;
    }

    public void setBreeds(Cat[] breeds) {
        this.breeds = breeds;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}
